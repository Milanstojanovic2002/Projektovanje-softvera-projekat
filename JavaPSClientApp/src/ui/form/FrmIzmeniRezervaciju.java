    private void popuniCmbPaneli() {
        try {
            List<Panel> paneli = Controller.getInstance().vratiPanele();

            for (Panel panel : paneli) {
                cmbPaneli.addItem(panel);
            }
            cmbPaneli.setSelectedIndex(-1);
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(FrmNewDelegat.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

    private void popuniCmbSale() {
        try {
            List<Sala> sale = Controller.getInstance().vratiSale();

            for (Sala s : sale) {
                cmbSale.addItem(s);
            }
            cmbSale.setSelectedIndex(-1);
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(FrmNewDelegat.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

    private void inicijalizujKalendar() {
        model = new UtilDateModel();
        Properties p = new Properties();
        JDatePanelImpl datePanel = new JDatePanelImpl(model, p);

        // Kreiranje JDatePicker
        datePicker = new JDatePickerImpl(datePanel, new DateLabelFormatter());

        
        panelDate.setLayout(null); 

        panelDate.setBounds(35, 10, 200, 30);
        datePicker.setBounds(35, 10, 200, 30);

        panelDate.add(datePicker);
    }
    
    private LocalDate dateToLocalDate(Date odabraniDatum) {
        return Instant.ofEpochMilli(odabraniDatum.getTime()).atZone(ZoneId.systemDefault()).toLocalDate();
    }

    private void popuniTabelu() {
        try {
            TableModelRezervacija modelRez = new TableModelRezervacija(rezervacije);
            tblSveRezervacije.setModel(modelRez);
        } catch (Exception e) {
            System.out.println("Tabela nije uspesno popunjena");
            e.printStackTrace();
        }
    }

    private void popuniTabeluPromenjenihRezervacija(List<Rezervacija> rezervacijeZaPromenu) {
        try {
            TableModelRezervacija modelrez = new TableModelRezervacija(rezervacijeZaPromenu);
            tblRezervacijePromena.setModel(modelrez);
        } catch (Exception ex) {
            System.out.println("Tabela nije uspesno popunjena");
            ex.printStackTrace();
        }
    }

    
