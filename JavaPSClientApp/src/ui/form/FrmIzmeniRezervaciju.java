    private void btnReserveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReserveActionPerformed
        
        Date izabraniDatum=(Date)model.getValue();
        localDate=dateToLocalDate(izabraniDatum);
        izabranaRezervacija.setDatum(localDate);
        izabranaRezervacija.setPanelid((Panel) cmbPaneli.getSelectedItem());
        izabranaRezervacija.setSalaid((Sala) cmbSale.getSelectedItem());
        izabranaRezervacija.setVremeOd(LocalTime.parse((CharSequence) cmbVreme.getSelectedItem()));
        LocalTime vremeOd=izabranaRezervacija.getVremeOd();
        LocalTime vremeDo=vremeOd.plusHours(2);
        rezervacijeZaPromenu.add(izabranaRezervacija);
        popuniTabeluPromenjenihRezervacija(rezervacijeZaPromenu);
    }//GEN-LAST:event_btnReserveActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        Date izabraniDatum=(Date)model.getValue();
        localDate=dateToLocalDate(izabraniDatum);
        System.out.println("Izabrani datum: " + localDate);

        Rezervacija rezervacija = new Rezervacija();
        rezervacija.setDatum(localDate);
        
        try {
            rezervacije = Controller.getInstance().nadjiRezervacijuPoDatumu(rezervacija);
            popuniTabelu();
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(FrmKreirajRezervaciju.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

